// operador and - && 
// operador or - || 

// ---------------------------- AND ----------------------------

// As duas condições devem ser verdadeiras 
const pipoca = true
const chocolate = true     

console.log(pipoca == true && chocolate == true)

// ---------------------------- OR ----------------------------

// Apenas umas das condições deve ser verdadeira
const caneta = true 
const lapis = false 

console.log(caneta == true || lapis == true)

// ---------------------------- NOT ----------------------------

// operador (!) 

const logado = false

console.log(`Valor original: ${logado}`)
console.log(`Valor invertido: ${!logado}`)

